package aglosh2014.appspot.com;

public class Circles {
	
	private int circle_id;
	private String  circle_name;
	
	public Circles(int id , String name)
	{
		circle_id = id;
		circle_name = name;
	}
	
	public int get_id()
	{
		return circle_id;
	}
	
	public String get_name()
	{
		return circle_name;
	}
	
	

}
